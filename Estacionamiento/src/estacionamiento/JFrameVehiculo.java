private void nuevo() throws Exception {
    try {
        Vehiculo v = new Vehiculo();
        v.setId_vehiculo(v.incrementovehiculo());
        jTextFieldId_vehiculo.setText(String.valueOf(v.getId_vehiculo()));
        jTextFieldMarca.setText("");
        jTextFieldModelo.setText("");
        jTextFieldPlaca.setText("");
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.toString());
    }
}

private void enviar() throws Exception {
    try {
        Vehiculo v = new Vehiculo();

        // Assuming you have an input field where the user can enter the id_usuario
        int idUsuario = Integer.parseInt(jTextFieldIDUsuario.getText());

        if (verificarClienteRegistrado(idUsuario)) {
            v.setId_usuario(idUsuario);
            v.setId_vehiculo(Integer.parseInt(jTextFieldId_vehiculo.getText()));
            v.setMarca(jTextFieldMarca.getText());
            v.setModelo(jTextFieldModelo.getText());
            v.setPlaca(jTextFieldPlaca.getText());

            v.insertarVehiculo(v);
            JOptionPane.showMessageDialog(null, "La información ha sido guardada con éxito");
        } else {
            JOptionPane.showMessageDialog(null, "El ID_USUARIO no existe en la tabla usuario");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.toString());
    }
    this.mostrar(jtCliente, "select * from vehiculo");
}

