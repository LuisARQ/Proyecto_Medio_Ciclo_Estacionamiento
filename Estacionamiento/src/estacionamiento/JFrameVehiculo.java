    private void mostrar(javax.swing.JTable JT, String sql) throws Exception {
    try {
        Usuario cb = new Usuario();
        ResultSet rs = cb.consultaTabla(sql);
        ResultSetMetaData rsMd = rs.getMetaData();
        int cantColumnas = rsMd.getColumnCount();
        
        DefaultTableModel modelo = new DefaultTableModel();
        JT.setModel(modelo);
        
        // Agregar las columnas al modelo de la tabla
        for (int i = 1; i <= cantColumnas; i++) {
            modelo.addColumn(rsMd.getColumnLabel(i));
        }
        
        // Agregar las filas al modelo de la tabla
        while (rs.next()) {
            Object[] columna = new Object[cantColumnas];
            for (int i = 0; i < cantColumnas; i++) {
                columna[i] = rs.getObject(i + 1);
            }
            modelo.addRow(columna);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.toString());
    }
}

